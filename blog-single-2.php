                <p style="font-size: 1rem; font-weight: bold; margin-bottom: 2px;">How to fix it: </p>
                <p>Shift 85 percent of the world’s energy supply to non-fossil fuel sources and invest in strategies like reforestation that capture carbon dioxide.</p>

