                <p style="font-size: 1rem; font-weight: bold; margin-bottom: 2px;">How we're taking action right now: </p>
                <p>
                    We’re analyzing satellite images and local yield potential to pinpoint where soy farming and cattle ranching can expand without destroying nature. This approach is especially vital in Brazil’s Cerrado region, where half of all natural habitat has already been converted to cropland and pasture. Cooperating with farmers on sustainable practices can help save what’s left of the Cerrado’s rich savanna.  
                </p>

                <p><strong style="font-weight: bold;">2. Eliminate Overfishing</strong></p>
                <p style="font-size: 1rem; font-weight: bold; margin-bottom: 2px;">Problem: </p>
                <p>Overfishing and poor fisheries management is not only devastating to the fish species being pushed to the brink of collapse. It endangers food webs and ocean ecosystems by disrupting the balance of all sea life. And it threatens billions of people who rely on seafood as an important source of livelihood and animal protein. Without serious changes, 84 percent of the world’s fish stocks will be in peril in our lifetime. </p>
                
                <p style="font-size: 1rem; font-weight: bold; margin-bottom: 2px;">How to fix it: </p>
                <p>Refine our fishing methods to only take what the fish populations can tolerate now, so our oceans can be more abundant and healtheir in the future.</p>
                
                <p style="font-size: 1rem; font-weight: bold; margin-bottom: 2px;">How we're taking action right now: </p>
                <p>We’re making it fast, easy and affordable for fishers to use data to manage their catches more sustainably. Like image recognition software, FishFace technology we’re pioneering in Indonesia uses artificial intelligence and machine learning to identify fish species and track their numbers so fishers can avoid catching too many or the wrong kind.</p>
              
                <p><strong style="font-weight: bold;">3. Increase Clean Energy</strong></p>
                <p style="font-size: 1rem; font-weight: bold; margin-bottom: 2px;">Problem: </p>
