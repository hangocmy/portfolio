              <strong>
                Some of these travel mishaps can be avoided and some of them are just a part of traveling. You simply cannot plan for everything. However, keeping a few important things in mind will make your travels much easier.
              </strong>
              <p><strong style="font-style: italic;">1. Park with your car nose out.</strong></p>
              <p>
                In the rush to catch the parking lot bus, it’s easy to leave an interior light on;
                I’d guess that more travelers I know have had dead batteries at an airport than in any other situation.
                If you return to your car to find a dead battery, broken windows due to thievery or any other potential problem,
                you’ll want your car to be parked nose out for easier access to the battery, or for an easier hook-up to a tow truck.
              </p>

              <p><strong style="font-style: italic;">2. Leave time to get to economy lots.</strong></p>
              <p>
                As airports expand, they need more parking spaces;
                those spaces are ever more frequently found in parking lots that are off-airport in every respect but name.
                You’ll also find that these lots are often significantly lower-priced than other lots.
                As a result, they’re the best place for economy-minded travelers,
                especially for longer trips where you’re racking up several days’ worth of parking fees.
                Also, these are the last lots to fill up;
                if you’re flying during peak travel periods, you may have no choice but to use these distant lots.
                I’ve found that buses and monorails run regularly to these lots,
                but I invariably need up to 20 to 30 minutes more than I might in less remote parking lots.
                If you’re looking to save money, or are traveling over a major holiday weekend,
                leave extra time to get from the lot to the terminal.
              </p>

              <p><strong style="font-style: italic;">3. Pack essentials in your carry-on.</strong></p>
              <p>
                Recent stats indicate that, on average, at least one bag on every flight is lost or delayed.
                If there’s anything you can’t live without, pack it in your carry-on.
                This is especially true of items that are not easily or inexpensively replaced,
                such as running shoes or a lightweight raincoat.
                And you’ll get through airport security faster if you pack your carry-on more efficiently.
                For example, have your quart-size plastic bag with liquids and gels packed in an outside pouch
                or right near the top of your bag so that you can easily pull it out for screening.
                See Packing Tips and What Not to Pack for more ideas.
              </p>

              <p><strong style="font-style: italic;">4. Know your hotel information.</strong></p>
              <p>
                If a) your baggage is lost or delayed;
                b) you miss your connection and will be late checking in;
                or c) you are going to a destination you’ve never visited before,
                you’ll want to have complete contact information for your hotel on your person.
                Before you leave home, print out the hotel’s name, address and phone number, and program the latter into your cell phone.
                It’s also a good idea to print out a map of the hotel’s neighborhood,
                whether for your own use or to show to a confused cab driver.
              </p>

              <p><strong style="font-style: italic;">5. Take old currency with you.</strong></p>
              <p>
                Exchanging foreign currency after you’ve returned home is a hassle,
                especially since almost no one spends any time in an actual bank these days.
                Why else do so many travelers have so much funny money lying around?
                If you travel abroad with any frequency, and have any stray foreign currency laying around,
                take it with you the next time you cross international borders.
                Then, when you get some local currency,
                you can exchange the money from any other country at the same time.
              </p>

              <p><strong style="font-style: italic;">6. Save your boarding pass.</strong></p>
              <p>
                Do you usually toss your boarding pass as soon as you step off the plane? You might want to reconsider.
                Your boarding pass can serve as proof of travel if your airline fails to give you the proper credit for frequent flier miles;
                this type of problem is particularly common if you’re flying on a codeshare partner of the airline in question.
                Your boarding pass can also be useful as a receipt for tax purposes, particularly if you’re self-employed.
              </p>

              <p><strong style="font-style: italic;">7. Get your seat assignments ASAP.</strong></p>
              <p>
                As I mention above, every minute you pass without a seat assignment is another minute that your aisle or window seat is given to someone else.
                Your best bet is to check in online, which can typically be done up to 24 hours before your flight.
                But note that not all flights, airlines or classes of travel permit advance check-in (or seating assignments).
              </p>

              <p><strong style="font-style: italic;">8. Mark your bags with an easily recognizable item.</strong></p>
              <p>
                The days of flower-pattern steamer trunks are long gone; now we all buy our bags at the same stores from the same manufacturers.
                The result: an endless stream of nearly identical bags on the baggage carousel.
                The solution: mark your bags by tying a colorful ribbon, stitching a unique patch or putting a large sticker on your bags.
                You won’t see other passengers pulling your bags off the carousel to check for their tiny name tags,
                and you’ll be able to see your suitcases come out the door from miles away.
              </p>

              <p><strong style="font-style: italic;">9. Remember your flight number.</strong></p>
              <p>This may seem like a no-brainer, but knowing your flight number can make your life easier in small or
                foreign airports that do not list the full names of destination airports,
                or list by flight number alone.</p>

